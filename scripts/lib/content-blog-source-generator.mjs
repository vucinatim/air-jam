import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";
import prettier from "prettier";

const __dirname = path.dirname(fileURLToPath(import.meta.url));

export const repoRoot = path.resolve(__dirname, "..", "..");
export const contentBlogRoot = path.join(repoRoot, "content", "blog");
export const outputFile = path.join(
  repoRoot,
  "apps",
  "platform",
  "src",
  "features",
  "blog",
  "generated",
  "content-blog.generated.ts",
);

export async function generateContentBlogSource() {
  const blogEntries = await collectBlogEntries(contentBlogRoot);
  const source = renderGeneratedSource(blogEntries);
  const prettierOptions = await prettier.resolveConfig(outputFile);

  return prettier.format(source, {
    ...prettierOptions,
    filepath: outputFile,
  });
}

export async function writeGeneratedContentBlogSource() {
  const source = await generateContentBlogSource();

  await fs.mkdir(path.dirname(outputFile), { recursive: true });
  await fs.writeFile(outputFile, source, "utf8");
}

export async function assertGeneratedContentBlogSourceIsFresh() {
  const expectedSource = await generateContentBlogSource();
  const currentSource = await fs.readFile(outputFile, "utf8");

  if (currentSource !== expectedSource) {
    throw new Error(
      `Generated blog source is stale at ${path.relative(
        repoRoot,
        outputFile,
      )}. Run "pnpm --filter platform blog:generate" and commit the result.`,
    );
  }
}

async function collectBlogEntries(rootDir) {
  const metaFiles = await findMetaFiles(rootDir);
  const entries = [];

  for (const metaFile of metaFiles) {
    const relativeMetaFile = path.relative(rootDir, metaFile);
    const relativeDir = path
      .dirname(relativeMetaFile)
      .replaceAll(path.sep, "/");
    const slug = relativeDir;
    const sourcePath = `content/blog/${relativeDir}/post.mdx`;
    const metaModulePath = `@content/blog/${relativeDir}/post.meta`;
    const mdxModulePath = `@content/blog/${relativeDir}/post.mdx`;
    const metaFileContents = await fs.readFile(metaFile, "utf8");
    const postExportName = extractPostExportName(metaFileContents, metaFile);
    const metadataExportName = `${postExportName.replace(/BlogPost$/, "")}Metadata`;

    entries.push({
      slug,
      sourcePath,
      metaModulePath,
      mdxModulePath,
      postExportName,
      metadataExportName,
    });
  }

  return entries.sort((left, right) => left.slug.localeCompare(right.slug));
}

async function findMetaFiles(dir) {
  const dirEntries = await fs.readdir(dir, { withFileTypes: true });
  const nestedFiles = await Promise.all(
    dirEntries.map(async (entry) => {
      const entryPath = path.join(dir, entry.name);

      if (entry.isDirectory()) {
        return findMetaFiles(entryPath);
      }

      if (entry.isFile() && entry.name === "post.meta.ts") {
        return [entryPath];
      }

      return [];
    }),
  );

  return nestedFiles.flat();
}

function extractPostExportName(fileContents, filename) {
  const match = fileContents.match(/export const (\w+BlogPost)\s*=/);

  if (!match) {
    throw new Error(
      `Could not find exported '*BlogPost' constant in ${filename}.`,
    );
  }

  return match[1];
}

function renderGeneratedSource(entries) {
  const importBlocks = entries
    .map(
      (entry) =>
        `import {\n  ${entry.postExportName},\n  metadata as ${entry.metadataExportName},\n} from "${entry.metaModulePath}";`,
    )
    .join("\n");

  const documents = entries
    .map(
      (entry) => `  {
    post: hydrateBlogPost(${entry.postExportName}, ${JSON.stringify(entry.slug)}),
    metadata: createBlogPostMetadata(
      hydrateBlogPost(${entry.metadataExportName}, ${JSON.stringify(entry.slug)}),
    ),
    sourcePath: ${JSON.stringify(entry.sourcePath)},
    loadComponent: () => import("${entry.mdxModulePath}"),
  },`,
    )
    .join("\n");

  return `// This file is generated by scripts/generate-content-blog-source.mjs.
// Do not edit it manually.

${importBlocks}

import { createBlogPostMetadata, hydrateBlogPost } from "../metadata";
import type { BlogSource } from "../source";

const generatedContentBlogDocuments = [
${documents}
];

export const generatedContentBlogSource: BlogSource = {
  name: "content-blog",
  loadDocuments() {
    return generatedContentBlogDocuments;
  },
};
`;
}
